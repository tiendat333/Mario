    public void updateTable(){
        try {
            String sqlSelect = "SELECT * FROM [LeaderBoard]";
            DAO conn = new DAO();
            Connection con = conn.makeConnection();
            Statement st;       
            
            st = con.createStatement(ResultSet.TYPE_SCROLL_INSENSITIVE, ResultSet.CONCUR_READ_ONLY);
            ResultSet rs = st.executeQuery(sqlSelect);
            
            vtcHead.setSize(0);
            vtcHead.add("Name");
            vtcHead.add("Score");
            
            vtcData.setSize(0);
            while (rs.next()){
                
                String name = rs.getString(1), score = rs.getString(2);
                Leader top = new Leader(name, score);
                Vector vtcRow = new Vector();
                vtcRow.add(top.Name);
                vtcRow.add(top.Score);
                vtcData.add(vtcRow);
            }   
            
            tblLeader.setModel(new DefaultTableModel(vtcData,vtcHead));
            
        } catch (SQLException ex) {
            Logger.getLogger(LeaderBoard.class.getName()).log(Level.SEVERE, null, ex);
        }  
    }


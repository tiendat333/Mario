    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        try {
            String sqlSelect = "SELECT * FROM [LeaderBoard]";
            DAO conn = new DAO();
            Connection con = conn.makeConnection();
            Statement st;
            st = con.createStatement(ResultSet.TYPE_SCROLL_INSENSITIVE, ResultSet.CONCUR_READ_ONLY);
            ResultSet rs = st.executeQuery(sqlSelect);
            
            boolean checkDup = false;
            while (rs.next()){
                if (txtName.getText().equals(rs.getString(1)))
                    checkDup = true;
            }
            
            if (txtName.getText().equals(""))
                JOptionPane.showMessageDialog(null,"Yêu cầu không được để trống Name");
            else if(checkDup == true){
                JOptionPane.showMessageDialog(null,"Yêu cầu không được nhập trùng Name");
                txtName.setText("");
            }else if(txtScore.getText().equals(""))
                JOptionPane.showMessageDialog(null,"Yêu cầu không được để trống Score");
            else{
                int dialogButton = JOptionPane.YES_NO_OPTION;
                int dialogResult = JOptionPane.showConfirmDialog(null, "Bạn có chắc muốn thêm không?", "THÔNG BÁO! ", dialogButton);
                if(dialogResult == 0) {  
                    String name = txtName.getText(), score = txtScore.getText();
                    PreparedStatement stmt = con.prepareStatement("insert into LeaderBoard (Name, Score) values (?, ?)");
                    stmt.setString(1, name);
                    stmt.setString(2, score);
                    stmt.executeUpdate();
            
                    JOptionPane.showMessageDialog(null,"Thêm thành công!");
                    clearAll();
                    updateTable();
                
                }
            }
            
        } catch (SQLException ex) {
        Logger.getLogger(LeaderBoard.class.getName()).log(Level.SEVERE, null, ex);
        } 
    }//GEN-LAST:event_btnAddActionPerformed

    
    
    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        try {
            String sqlSelect = "SELECT * FROM [LeaderBoard]";
            DAO conn = new DAO();
            Connection con = conn.makeConnection();
            Statement st;
            st = con.createStatement(ResultSet.TYPE_SCROLL_INSENSITIVE, ResultSet.CONCUR_READ_ONLY);
            ResultSet rs = st.executeQuery(sqlSelect);
            if (txtName.getText().equals(""))
                    JOptionPane.showMessageDialog(null,"Yêu cầu không được để trống Name");
            else{
                 
                    boolean checkDup = false;
                    while (rs.next()){
                        if (txtName.getText().equals(rs.getString(1)))
                            checkDup = true;
                    }
                    if (checkDup == true){
                        int dialogButton = JOptionPane.YES_NO_OPTION;
                        int dialogResult = JOptionPane.showConfirmDialog(null, "Bạn có chắc muốn xóa không?", "THÔNG BÁO! ", dialogButton);
                        if(dialogResult == 0) { 
                            PreparedStatement stmt = con.prepareStatement("DELETE FROM LeaderBoard WHERE Name =" + txtName.getText() +"");
                            stmt.execute();
                            JOptionPane.showMessageDialog(null,"Xóa thành công!");
                            txtName.setText("");
                        }
                    }else{
                        JOptionPane.showMessageDialog(null,"Không tìm thấy Name");
                        txtName.setText("");
                    }
                
            }
        } catch (SQLException ex) {
            Logger.getLogger(LeaderBoard.class.getName()).log(Level.SEVERE, null, ex);
        }  
    }//GEN-LAST:event_btnDeleteActionPerformed

